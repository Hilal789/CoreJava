package PriorityQueue_04_06_25;

import java.util.PriorityQueue;

public class Q1 {

	public static void main(String[] args) {
		
		
		PriorityQueue<String> pq = new PriorityQueue<>();
		pq.add("Red");
		pq.add("Green");
		pq.add("Orange");
		pq.add("White");
		pq.add("Black");
	

	pq.forEach(System.out::println);
		
	
		
		
		
		
		
		
	}

}
